package week1.day1;

public class FactorialNumber {

	public static void main(String[] args) {
		int x=5;
		int fact=1;
		
		for (int i = 1; i <=x; i++) {
			
			fact=fact*i;
			
		}

System.out.println("The factorial of the given number is " + fact);
	}
}

